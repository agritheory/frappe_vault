@click.command("vault-status")
@pass_context
def vault_status(context):
	"""Check OpenBao connectivity and configuration status."""
	site = get_site(context)
	frappe.init(site=site)
	frappe.connect()

	try:
		print(f"Site: {site}")
		print(f"enable_vault_secrets: {frappe.conf.get('enable_vault_secrets', False)}")
		print(f"enable_vault_user_passwords: {frappe.conf.get('enable_vault_user_passwords', False)}")
		print(f"vault_url: {frappe.conf.get('vault_url', 'not set')}")
		print(f"vault_proxy_enabled: {frappe.conf.get('vault_proxy_enabled', False)}")

		from frappe_vault.vault_client import get_vault_client

		try:
			client = get_vault_client()
			available = client.is_available()
			print(f"\nOpenBao Status: {'✓ Connected' if available else '✗ Not Available'}")
			if available:
				health = client.check_health()
				print(f"  Initialized: {health.get('initialized', 'unknown')}")
				print(f"  Sealed: {health.get('sealed', 'unknown')}")
		except Exception as e:
			print(f"\nOpenBao Status: ✗ Error - {e}")

		# Count passwords in __Auth
		Auth = frappe.qb.Table("__Auth")
		auth_count = (
			frappe.qb.from_(Auth)
			.select(frappe.qb.functions.Count("*"))
			.where(Auth.password.isnotnull())
			.run()
		)[0][0]
		print(f"\nPasswords in __Auth table: {auth_count}")

	finally:
		frappe.destroy()


def _get_bench_path():
	"""Get the bench path, works both inside and outside frappe context."""
	try:
		from frappe.utils import get_bench_path

		return get_bench_path()
	except Exception:
		# Fallback: walk up from current directory looking for sites folder
		cwd = os.getcwd()
		while cwd != "/":
			if os.path.isdir(os.path.join(cwd, "sites")):
				return cwd
			cwd = os.path.dirname(cwd)
		return os.getcwd()


def _write_file_secure(path: str, content: str, mode: int = 0o600) -> None:
	"""Write a file with secure permissions."""
	# Create parent directory if needed
	parent_dir = os.path.dirname(path)
	if parent_dir and not os.path.exists(parent_dir):
		os.makedirs(parent_dir, mode=0o755)

	# Write with restrictive permissions
	fd = os.open(path, os.O_WRONLY | os.O_CREAT | os.O_TRUNC, mode)
	try:
		os.write(fd, content.encode())
	finally:
		os.close(fd)


def _generate_openbao_config(
	seal_key_path: str, data_path: str, audit_log_path: str, production: bool = False
) -> str:
	"""Generate OpenBao configuration file content."""
	if production:
		# Production: quieter, structured logging for log aggregation
		log_level = "warn"
		log_format = "json"
	else:
		# Development: verbose, human-readable in console
		log_level = "info"
		log_format = "standard"

	return f"""# OpenBao configuration for Frappe Vault
# Generated by: bench setup-openbao
# Documentation: https://openbao.org/docs/configuration

ui = true

log_level = "{log_level}"
log_format = "{log_format}"

storage "file" {{
  path = "{data_path}"
}}

listener "tcp" {{
  address     = "127.0.0.1:8200"
  tls_disable = true  # TLS handled by nginx reverse proxy
}}

# Static seal for auto-unseal on restart
# This allows OpenBao to unseal automatically after bench restart
seal "static" {{
  current_key_id = "frappe-vault-1"
  current_key = "file://{seal_key_path}"
}}

# Audit logging - all requests/responses are logged
audit_device "file" {{
  path      = "file"
  file_path = "{audit_log_path}"
}}

api_addr = "http://127.0.0.1:8200"
"""


def _generate_supervisor_config(config_path: str, bench_path: str, user: str) -> str:
	"""Generate supervisor configuration for OpenBao."""
